std::string	toUpper(const std::string &str)
{
	// UTF-8 toLower
	std::string res;
	res.reserve(str.size() + (str.size() >> 2));
	const char *cstr = &str[0];
	for (ptrdiff_t i = 0; i < (ptrdiff_t)str.size();)
		appendToUpperAsUtf8(res, cstr, i);
	return res;
}

