def addTags(sub, depth, tags):
	for i in range(len(tags) - 1, -1, -1):
		tag = tags[i]
		if tag in scheme:
			s = scheme[tag]
			largest = 0
			for k in s[depth]:
				if len(k) >= largest and sub.startswith(k):
					largest = len(k)
			if largest > 0:
				for k in s[depth]:
					if len(k) >= largest and sub.startswith(k):
						tags += s[depth][k].split(" ")
						return k
	return ""

def parse(name):
	tags = [ "" ]
	sub = name
	depth = 0
	while len(sub) > 0:
		add = addTags(sub, depth, tags)
		if len(add) == 0:
			return tags[1:] + [ "incomplete", "_" + sub ]
		sub = sub[len(add):]
		depth += 1
	return tags[1:]

